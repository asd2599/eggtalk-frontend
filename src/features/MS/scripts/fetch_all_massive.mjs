const KAKAO_API_KEY = process.env.VITE_KAKAO_MAP_KEY;
const ORIGIN = 'http://localhost:5173';

async function run() {
  if (!KAKAO_API_KEY) {
    console.error('[CRITICAL] VITE_KAKAO_MAP_KEY missing!');
    return;
  }

  let out = {};
  const uniqueStations = [...new Set(target_stations)];
  console.log('Total unique stations to check/fetch:', uniqueStations.length);

  let text = fs.readFileSync('src/features/MS/subwayCoords.js', 'utf8');

  for (let stn of uniqueStations) {
    if (text.includes(`"${stn}":`) || text.includes(`  ${stn}:`)) {
      continue;
    }

    console.log('Fetching: ' + stn);
    try {
      const res = await fetch(
        `https://dapi.kakao.com/v2/local/search/keyword.json?query=${encodeURIComponent(stn + '역')}`,
        {
          headers: {
            Authorization: `KakaoAK ${KAKAO_API_KEY}`,
            Referer: ORIGIN + '/',
            Origin: ORIGIN,
            KA: `sdk/1.0.0 os/javascript lang/ko-KR res/1920x1080 device/web_browser origin/${encodeURIComponent(ORIGIN)}`,
          },
        },
      );
      const data = await res.json();
      const docs = data.documents || [];
      let found = false;
      for (let doc of docs) {
        if (
          doc.category_group_code === 'SW8' ||
          doc.category_name.includes('지하철')
        ) {
          out[stn] = { lat: parseFloat(doc.y), lng: parseFloat(doc.x) };
          found = true;
          break;
        }
      }
      if (!found && docs.length > 0) {
        out[stn] = { lat: parseFloat(docs[0].y), lng: parseFloat(docs[0].x) };
      }
    } catch (e) {
      console.error(stn, e.message);
    }
    await new Promise((r) => setTimeout(r, 60));
  }

  text = text.replace('};\n', '').replace('};\r\n', '').replace('};', '');
  text +=
    '\n  // --- 전 노선(1~9호선, 수인분당, 신분당) 풀 커버리지 반영 ---\n';
  let added = 0;
  for (let [k, v] of Object.entries(out)) {
    text += `  "${k}": { lat: ${v.lat}, lng: ${v.lng} },\n`;
    added++;
  }
  text += '};\n';

  fs.writeFileSync('src/features/MS/subwayCoords.js', text);
  console.log(
    `\nDONE! Automatically fetched and added ${added} missing stations!`,
  );
}

run();
